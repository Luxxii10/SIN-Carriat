# Les boucles iteratives
for voyelle in ['a', 'e', 'i', 'o', 'u', 'y']: # exécute le script avec voyelle = une entrée de la table, qui change dans l'ordre à chaque exécution
    print(voyelle)

for n in range (10): # exécute le script dans la boucle avec n incrémenté à partir de 0 jusqu'à 9 (10 est ignoré)
    print(n)

for n in range (2, 7): # exécute le script dans la boucle avec n incrémenté à partir de 2 jusqu'à 6 (7 est ignoré)
    print(n)

for pair in range (100, 110, 2): # exécute le script dans la boucle avec pair qui prend une valeur sur deux entre 100 et 110 (en excluant 110)
    print(pair)
